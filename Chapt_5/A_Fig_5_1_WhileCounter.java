package Chapt_5;

public class A_Fig_5_1_WhileCounter {
    public static void main(String[] args) {
        
        int counter = 1;
        
        while(counter <= 10){
            System.out.printf("%d ",counter);
            ++counter;
        }
        
        System.out.println();
    }
}
