public class E_Fig_5_7_DoWhileTest {
    public static void main(String[] args) {
        
        int counter = 1;
        
        do{
            System.out.printf("%d ", counter);
            ++counter;
        }while(counter <= 10);
        
        System.out.println();
    }
}
